int		ft_sqrt(int nb)
{
	int i;
	int mid;
	int total;

	i = 0;
	mid = nb / 2;
	while (i < mid && total != nb)
	{
		i++;
		total = i * i;
	}
	if (total == nb)
	{
		return (i);
	}
	else
		return (0);
}
