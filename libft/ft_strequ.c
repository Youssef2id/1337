#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (ft_strlen(s1) != ft_strlen(s2))
		return (0);
	while (s1[i] == s2[i] && i < ft_strlen(s1))
	{
		i++;
	}
	if (s1[i] != s2[i])
		return (0);
	return (1);
}
